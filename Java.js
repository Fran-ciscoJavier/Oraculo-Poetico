function revelarDestino(emocion, colorFondo) {
    const lista = biblioteca[emocion];
    const azar = Math.floor(Math.random() * lista.length);
    const poemaElegido = lista[azar];
    
    // 1. Cambiamos el fondo y las pantallas
    document.body.style.backgroundColor = colorFondo;
    document.getElementById('inicio').classList.remove('activa');
    document.getElementById('resultado').classList.add('activa');

    // 2. Preparamos el contenedor
    const contenedor = document.getElementById('poema-contenido');
    contenedor.innerHTML = ""; // Lo dejamos vacío para empezar a escribir
    
    let i = 0;

    // 3. Función interna de escritura
    function escribir() {
        if (i < poemaElegido.length) {
            // Si detecta un link ( <a ), lo pone todo de golpe y para la animación
            if (poemaElegido.slice(i, i + 2) === "<a") {
                contenedor.innerHTML += poemaElegido.slice(i);
                return; 
            }
            
            // Si es texto normal, pone una letra
            contenedor.innerHTML += poemaElegido.charAt(i);
            i++;
            setTimeout(escribir, 50); // Velocidad: 40ms por letra
        }
    }
    
    escribir(); // Inicia la animación
}


